// ---- ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ ----
// Эти функции используются во всех остальных файлах.

// $ — быстрый поиск элемента на странице по CSS-селектору
// Пример: var кнопка = $('.my-button'); // найдёт элемент с классом "my-button"
function $(selector, parent) {
    return (parent || document).querySelector(selector);
}

// $$ — поиск ВСЕХ элементов по CSS-селектору, возвращает массив
// Пример: var всеКнопки = $$('.btn'); // найдёт ВСЕ элементы с классом "btn"
function $$(selector, parent) {
    // Array.from() превращает NodeList в обычный массив, чтобы работали .map(), .forEach() и т.д.
    return Array.from((parent || document).querySelectorAll(selector));
}

// escapeHTML — защита от XSS-атак: превращает <, >, & в безопасный текст
// Нужна, когда мы выводим на экран текст, введённый пользователем
function escapeHTML(str) {
    var div = document.createElement('div');
    div.textContent = str;  // textContent автоматически экранирует HTML-теги
    return div.innerHTML;
}

// formatText — превращает «простой текст» в HTML с форматированием:
//   **жирный**  → <strong>жирный</strong>
//   *курсив*    → <em>курсив</em>
//   \n (перенос строки) → <br>
function formatText(text) {
    return text
        .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')  // Замена **...** на жирный
        .replace(/\*(.*?)\*/g, '<em>$1</em>')               // Замена *...* на курсив
        .replace(/\n/g, '<br>');                             // Замена переносов строк
}

// formatTime — превращает число секунд в строку вида ":05" или ":15"
// Пример: formatTime(5) → ":05", formatTime(15) → ":15"
function formatTime(seconds) {
    if (seconds < 10) {
        return ':0' + seconds;
    } else {
        return ':' + seconds;
    }
}

// getRandom — возвращает случайный элемент из массива
// Пример: getRandom(['a', 'b', 'c']) → может вернуть 'a', 'b' или 'c'
function getRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

// shuffle — перемешивает массив (алгоритм Фишера-Йетса, «тасование колоды»)
// Возвращает НОВЫЙ перемешанный массив, оригинал не меняет
function shuffle(arr) {
    var a = arr.slice();  // Копируем массив, чтобы не менять оригинал
    for (var i = a.length - 1; i > 0; i--) {
        var j = Math.floor(Math.random() * (i + 1));
        // Меняем местами a[i] и a[j] (classic swap trick)
        var temp = a[i];
        a[i] = a[j];
        a[j] = temp;
    }
    return a;
}

// atomic — «атомарная» операция: запрещает запускать другое действие,
// пока текущее не завершится. Защита от двойных нажатий и гонок.
// Пример: await atomic(async function() { ... });
// Если atomic уже выполняется, вернёт false и не запустит новую функцию.
function atomic(fn) {
    // state.atomicAction — глобальная переменная. Мы будем использовать её
    // из файла state.js, но она уже объявлена там.
    // window.state должен быть доступен к моменту вызова.
    var st = window.state;
    if (st.atomicAction) {
        return false;  // Уже выполняется какая-то атомарная операция
    }
    st.atomicAction = true;
    try {
        fn();  // Запускаем переданную функцию
        return true;
    } finally {
        st.atomicAction = false;  // Снимаем блокировку в любом случае (даже если была ошибка)
    }
}